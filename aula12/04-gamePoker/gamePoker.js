function choose() {
    let random = [];


    for (i = 0; i < 5; i++) {

        let count = 0;
        let preRandom = Math.floor(Math.random() * deckCards.length);
        // console.log(preRandom)

        for (j = 0; j < random.length; j++) {

            if (preRandom == random[j]) {
                count++;
                i--;
            };
        };

        if (count == 0) {
            random.push(preRandom);
        };

        console.log(random)
        
    };
    // console.log(random)
    // console.log("###")
    orderCards(random);
    console.log(deckCards[random[0]].value)
    return random;
};

